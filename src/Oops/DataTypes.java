package Oops;



public class DataTypes {

	// Data type is known as what type of data is stored into a variable.
	//Data type are classified into two types
	//1.Primitive  2.Non-Primitive
	//If any keyword act as a data type those are primitive data types.
	//If any class act as a data type those are non primitive data types.
	

	public static void main(String[] args) {

		// Primitive data types
		byte num1=7; //Represents Integer values.
		short num2=10; //represents integer values.
		int num3=18; // Represents Integer values.
		long num4=17; //Represents Integer values.
		double num5=99.0; // Represents Decimal values.
		float num6=100.0f;// Represents Decimal Values
		char c1='A'; // Every character have its own unique ASCI Value.
		boolean b1=true;//It represents only true or false values.
		
		//Non-Primitive Data type
		String s1="Duryodhana";
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		System.out.println(num5);
		System.out.println(num6);
		System.out.println(c1);
		System.out.println(b1);
		System.out.println(s1);
		

	}

}

